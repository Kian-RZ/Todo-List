<script>
import axios from "axios";
import { ref, reactive } from "vue";

export default {
  setup() {
    const todos = ref([]);
    let add = reactive({
      data: "",
    });
    let edit = reactive({
      isEditing: false,
      style: "",
      editId: 0,
    });
    let status = reactive({
      noPost: false,
    });
    function getTodos() {
      axios.get("http://127.0.0.1:8000/list/").then(function (response) {
        if (response.data["status"] != "ok") {
          status.noPost = true;
        } else {
          status.noPost = false;
        }
        delete response.data.status
        todos.value = response.data;
      });
    }

    function check(index) {
      if (edit.isEditing == false) {
        axios.get(`http://127.0.0.1:8000/check/${index}`).then(function () {
          getTodos();
        });
      }
    }

    function del(index) {
      if (edit.isEditing == false) {
        axios.get(`http://127.0.0.1:8000/delete/${index}`).then(function () {
          getTodos();
        });
      }
    }

    function back(index) {
      if (edit.isEditing == false) {
        axios.get(`http://127.0.0.1:8000/back/${index}`).then(function () {
          getTodos();
        });
      }
    }

    function addTodo() {
      if (edit.isEditing == false) {
        if (add.data.trim() != "") {
          axios
            .get(`http://127.0.0.1:8000/create/${add.data}`)
            .then(function () {
              getTodos();
              add.data = "";
            });
        }
      }
    }

    function editTodo() {
      if (edit.isEditing == true) {
        if (add.data.trim() != "") {
          axios
            .get(`http://127.0.0.1:8000/edit/${edit.editId}/${add.data}`)
            .then(function () {
              getTodos();
              add.data = "";
              edit.isEditing = false;
            });
        }
      }
    }

    function exitEditing() {
      if (edit.isEditing == true) {
        edit.isEditing = false;
        add.data = "";
      }
    }

    function editOpen(id, title) {
      if (edit.isEditing == false) {
        edit.isEditing = true;
        edit.editId = id;
        add.data = title;
      }
    }

    getTodos();
    return {
      todos,
      check,
      del,
      back,
      addTodo,
      add,
      edit,
      exitEditing,
      editOpen,
      editTodo,
      status,
    };
  },
  updated() {
    if (this.edit.isEditing == true) {
      this.edit.style =
        "background-color: #cccccc;border-color: #cccccc;cursor: not-allowed;";
    } else {
      this.edit.style = "";
    }
  },
};
</script>

